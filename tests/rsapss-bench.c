void rsa_pss_sign_2048() {

  cycles a,b;
  clock_t t1,t2;

  uint32_t modBits = 2048;
  uint8_t *nb = test_n;
  uint32_t eBits = 24;
  uint8_t *e = test_e;
  uint32_t dBits = 2048;
  uint8_t *db = test_d;
  uint32_t msgLen = sizeof(test_msg)/sizeof(uint8_t);
  uint8_t *msg = test_msg;
  uint32_t saltLen = 20;
  uint8_t *salt = test_salt;
  uint8_t *sgnt_expected = test_sgnt_expected;

  uint32_t nbLen = (modBits - (uint32_t)1U) / (uint32_t)8U + (uint32_t)1U;
  uint8_t sgnt[nbLen];

  uint64_t *pkey = Hacl_RSAPSS_new_rsapss_load_pkey(modBits, eBits, nb, e);
  uint64_t *skey = Hacl_RSAPSS_new_rsapss_load_skey(modBits, eBits, dBits, nb, e, db);


    for (int j = 0; j < ROUNDS; j++) {
      Hacl_RSAPSS_rsapss_sign(Spec_Hash_Definitions_SHA2_256, modBits, eBits, dBits, skey, saltLen, salt, msgLen, msg, sgnt);
    }

    t1 = clock();
    a = cpucycles_begin();
    for (int j = 0; j < ROUNDS; j++) {
      Hacl_RSAPSS_rsapss_sign(Spec_Hash_Definitions_SHA2_256, modBits, eBits, dBits, skey, saltLen, salt, msgLen, msg, sgnt);
    }
    b = cpucycles_end();
    t2 = clock();

  uint64_t count = ROUNDS;

  double diff = t2 - t1;
  uint64_t cyc = b - a;

  printf("Benchmark RSA_PSS_sign (2048 bits)\n");
  print_time_ops(count, diff, cyc);

  return;
}


